    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        PesquisaProdutoView pesquisaProdutoView = new PesquisaProdutoView(this, true);
        pesquisaProdutoView.setLocationRelativeTo(this);
        pesquisaProdutoView.setVisible(true);
        if (pesquisaProdutoView.isProdutoSelecionado()) {
            Produto produto = pesquisaProdutoView.getProduto();
            txtID.setText(String.valueOf(produto.getId()));
            txtCodBarra.setText(String.valueOf(produto.getCodigoBarra()));
            txtNomeProduto.setText(produto.getNome());
            txtValorUnitario.setText(String.valueOf(produto.getValorUnitario()));
            txtEstoque.setText(String.valueOf(produto.getEstoque()));
            habilitaAtualizar();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtCodBarra.getText().trim().equals("")
                && txtNomeProduto.getText().trim().equals("")
                && txtValorUnitario.getText().trim().equals("")
                && txtEstoque.getText().trim().equals("")
                && cbCategoria.getSelectedItem() == null) {
            Messages.alertMessage(this, "Todos os campos são obrigatórios!");
        } else {
            Produto produto = new Produto();
            produto.setNome(txtNomeProduto.getText());
            produto.setCodigoBarra(Integer.parseInt(txtCodBarra.getText()));
            produto.setValorUnitario(Double.parseDouble(txtValorUnitario.getText().replace(",", ".")));
            produto.setEstoque(Integer.parseInt(txtEstoque.getText()));

            if (new ProdutoController().salvar(produto)) {
                clearFields();
                Messages.infoMessage(this, "Produto salvo!");
            } else {
                Messages.errorMessage(this, "Não foi possível realizar esta operação!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (txtCodBarra.getText().trim().equals("")
                && txtNomeProduto.getText().trim().equals("")
                && txtValorUnitario.getText().trim().equals("")
                && txtEstoque.getText().trim().equals("")
                && cbCategoria.getSelectedItem() == null) {
            Messages.alertMessage(this, "Todos os campos são obrigatórios!");
        } else {
            Produto produto = new Produto();
            produto.setId(Integer.parseInt(txtID.getText()));
            produto.setNome(txtNomeProduto.getText());
            produto.setCodigoBarra(Integer.parseInt(txtCodBarra.getText()));
            produto.setValorUnitario(Double.parseDouble(txtValorUnitario.getText()));
            produto.setEstoque(Integer.parseInt(txtEstoque.getText()));

            if (new ProdutoController().atualizar(produto)) {
                clearFields();
                habilitaSalvar();
                Messages.infoMessage(this, "Produto atualizado!");
            } else {
                Messages.errorMessage(this, "Não foi possível realizar esta operação!");
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

