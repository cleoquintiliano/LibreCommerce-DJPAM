    public void habilitaAtualizar() {
        this.btnAtualizar.setEnabled(true);
        this.btnSalvar.setEnabled(false);
    }

    public void habilitaSalvar() {
        this.btnAtualizar.setEnabled(false);
        this.btnSalvar.setEnabled(true);
    }

    public void clearFields() {
        this.txtID.setText("");
        this.txtCodBarra.setText("");
        this.txtNomeProduto.setText("");
        this.txtValorUnitario.setText("");
        this.txtEstoque.setText("");
    }

