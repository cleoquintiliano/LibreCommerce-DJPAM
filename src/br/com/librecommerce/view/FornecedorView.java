    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        PesquisaFornecedorView pesquisaFornecedorView = new PesquisaFornecedorView(this, true);
        pesquisaFornecedorView.setLocationRelativeTo(this);
        pesquisaFornecedorView.setVisible(true);
        if (pesquisaFornecedorView.isFornecedorSelecionado()) {
            Fornecedor fornecedor = pesquisaFornecedorView.getFornecedor();
            txtID.setText(String.valueOf(fornecedor.getId()));
            txtNomeFornecedor.setText(fornecedor.getNome());
            txtEndereco.setText(fornecedor.getEndereco());
            txtCNPJ.setText(fornecedor.getCnpj());
            txtInscEstadual.setText(fornecedor.getInscEstadual());
            habilitaAtualizar();
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomeFornecedor.getText().trim().equals("") || cbCidades.getSelectedItem() == null) {
            Messages.alertMessage(this, "Informe o Nome e a Cidade!");
        } else {
            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setNome(txtNomeFornecedor.getText());
            fornecedor.setEndereco(txtEndereco.getText());
            fornecedor.setCidade((Cidade) cbCidades.getSelectedItem());
            fornecedor.setCnpj(txtCNPJ.getText());
            fornecedor.setInscEstadual(txtInscEstadual.getText());
            if (new FornecedorController().salvar(fornecedor)) {
                clearFields();
                Messages.infoMessage(this, "Fornecedor salvo!");
            } else {
                Messages.errorMessage(this, "N√£o foi poss√≠vel realizar esta opera√ß√£o!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (txtNomeFornecedor.getText().trim().equals("") || cbCidades.getSelectedItem() == null) {
            Messages.alertMessage(this, "Informe o Nome e a Cidade!");
        } else {
            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setId(Integer.parseInt(txtID.getText()));
            fornecedor.setNome(txtNomeFornecedor.getText());
            fornecedor.setEndereco(txtEndereco.getText());
            fornecedor.setCidade((Cidade) cbCidades.getSelectedItem());
            fornecedor.setCnpj(txtCNPJ.getText());
            fornecedor.setInscEstadual(txtInscEstadual.getText());
            if (new FornecedorController().atualizar(fornecedor)) {
                clearFields();
                habilitaSalvar();
                Messages.infoMessage(this, "Fornecedor atualizado!");
            } else {
                Messages.errorMessage(this, "N√£o foi poss√≠vel realizar esta opera√ß√£o!");
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

